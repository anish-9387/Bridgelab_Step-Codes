// Class creation
class KilometerToMileConverter{
	public static void main(String[] args){
		// Creating variable for storing kilometer
		double kilometer = 10.8;
		
		// Calculating kilometers in miles
		double miles = kilometer * 1.6;
		
		// Displaying the result
		System.out.println("The distance " + kilometer + "km in miles is " + miles);
	}
}
